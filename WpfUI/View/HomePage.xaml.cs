    /// <summary>
    /// HomePage.xaml 的交互逻辑
    /// </summary>
    public partial class HomePage : UserControl
    {
        public HomePage()
        {
            InitializeComponent();
        }

        private void TextBox_KeyDown(object sender, KeyEventArgs e)
        {
            if (e.Key == Key.Enter)
            {
                Window win = new Window();
                CtlFlowView dtlview = new CtlFlowView();
                win.Content = dtlview;
                win.ShowDialog();
            }
        }
    }
